PARSED = [(pd.Timestamp(e[0]), e[1], e[2], e[3], e[4], e[5], e[6]) for e in EVENTS]


def generate(freq, start, end):
    dates = pd.date_range(start=start, end=end, freq=freq)
    df = pd.DataFrame(index=dates)
    n = len(dates)
    dates_arr = dates.values
    for m_idx, metal in enumerate(METALS):
        sentiment = np.zeros(n, dtype=np.float32)
        for i in range(n):
            dt = dates_arr[i]
            s = 0.0
            for p in PARSED:
                diff = int((pd.Timestamp(dt) - p[0]).days)
                if diff < -3 or diff >= p[6]:
                    continue
                val = p[1 + m_idx]
                if diff >= 0:
                    s += val * (1.0 - diff / p[6])
                else:
                    s += val * 0.3 * (1.0 + diff / 3.0)
            sentiment[i] = min(max(s, -1.0), 1.0)
        col = f"news_{metal}"
        s = pd.Series(sentiment)
        ema5 = s.ewm(span=5).mean().values.astype(np.float32)
        df[col] = sentiment
        df[f"{col}_ema5"] = ema5
        df[f"{col}_shock"] = (s - ema5).fillna(0).values.astype(np.float32)
    df.index.name = "time"
    return df


